print(f"   - 드론전용: {zone_counts.get('드론전용',0)}셀")
print(f"   - 로봇전용: {zone_counts.get('로봇전용',0)}셀")
print(f"   - Hybrid:   {zone_counts.get('Hybrid',0)}셀")
print(f"   - 일반:     {zone_counts.get('일반',0)}셀")
print(f"   - 부적합:   {zone_counts.get('부적합',0)}셀")
